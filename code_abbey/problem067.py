def generate_fib_dict(fib_nums, fiblist=FIBLIST):
    while len(fiblist) < fib_nums:
        fib_big = fiblist[-1]
        fib_small = fiblist[-2]
        fib_new = fib_big + fib_small
        fiblist.append(fib_new)
    fibdict = {key: value for value, key in enumerate(fiblist)}
    with open(FIBFILE, 'w') as file:
        file.write(json.dumps(fibdict))
    return


def get_index_from_dict(fib_num, fibfile=FIBFILE):
    fibdict = json.load(open(fibfile, 'r'))
    return fibdict[f'{fib_num}']


def read_input(data=DATA):
    datalist = data.splitlines()
    datalist = [int(x) for x in datalist]
    return datalist


