ADJACENT_NUMBERS = 13

def adjacent_product(number, adjacent_numbers):
    """Determines the biggest product in NUMBER for a product of size
    ADJACENT_NUMBERS. If ADJACENT_NUMBERS = 3, then 3 digits are multiplied.
    For example :
    number = 9812345
    adjacent_numbers = 4  # that means we try the following products:
        {9812}347 (9*8*1*2 = 144), 9{8123}47 (8*1*2*3 = 48),
        98{1234}7 (1*2*3*4 = 24), 981{2347} (2*3*4*7 = 168)
    The biggest product of 4 adjacent numbers in 9812347 is 168."""
    length_number = len(number)  # how many digits in NUMBER
    number = [int(x) for x in list(number)]
    product_list = []

    for index, _ in enumerate(number):
        if index <= (length_number - adjacent_numbers):
            start_range = index  # index is included
            end_range = index + adjacent_numbers  # index is excluded
            product = 1
            for index, digit in enumerate(number[start_range:end_range]):
                product = product * digit
            product_list.append(product)
    return max(product_list)


